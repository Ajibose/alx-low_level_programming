#include "operations.h"

/**
 * add - add two numbers
 * @n1: first number
 * @n2: second number
 *
 * Return: sum of two numbers
 */
int add(int n1, int n2)
{
	return (n1 + n2);
}

/**
 * sub - perform a subtraction operation on two numbers
 * @n1: first number
 * @n2: second number
 *
 * Return: difference of the two numbers
 */
int sub(int n1, int n2)
{
	return (n1 - n2);
}

/**
 * mul - perform a multiplication operation on two numbers
 * @n1: first number
 * @n2: second number
 *
 * Return: result of multiplication
 */
int mul(int n1, int n2)
{
	return (n1 * n2);
}

/**
 * div - perform a division operation on two numbers
 * @n1: first number
 * @n2: second number
 *
 * Return: result of division
 */
int div(int n1, int n2)
{
	return (n1 / n2);
}
